
public class Point {
    private double x,y;
 
    public Point( double initX, double initY) {
        x = initX;
        y = initY;
    }
   
    public double getX() {
        return x;
    }
 
    public double getY() {
        return y;
    }
 
 
    public void setX(float newX) {
        x = newX;
    }
 
    public void setY(float newY) {
        y = newY;
    }
 
}
